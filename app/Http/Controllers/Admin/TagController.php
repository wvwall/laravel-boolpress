    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:255'
          ]);
    
          $data = $request->all();
          $tag = new Tag();

          $slug = Str::slug($tag->name. '-' );
          $slug_base = $slug;
          $contatore = 1;
          $tag_slug = Tag::where('slug', '=', $slug)->first();
          while($tag_slug) {
              $slug = $slug_base . '-' .$contatore;
              $contatore++;
  
              $tag_slug = Tag::where('slug', '=', $slug)->first();
          }
          $tag->slug = $slug;
          $tag->save();
          
          
    
          return redirect()->route('admin.tags.index');
    }

